uint64_t lcg_fill(void *buf, size_t buf_sz, uint64_t x)
{
  size_t i;

  for (i = 0; i < buf_sz; i++)
  {
    /*  arbitrary LCG parameters */
    /*  nuclear.llnl.gov/CNP/rng/rngman.pdf */
    x = 2862933555777941757 * x + 3037000493;
    ((uint8_t *)buf)[i] = x >> 56;
  }

  return x;
}

void hash_count_clear()
{
  sha2_256_compress_count = 0;
  sha2_512_compress_count = 0;
  keccak_f1600_count = 0;
}

uint64_t hash_count()
{
  uint64_t tot;
  tot = sha2_256_compress_count + sha2_512_compress_count + keccak_f1600_count;
  /*
          printf("time: %lu  (", tot);
          if (sha2_256_compress_count > 0) {
                  printf(" SHA256: %lu", sha2_256_compress_count);
          }
          if (sha2_512_compress_count > 0) {
                  printf(" SHA512: %lu", sha2_512_compress_count);
          }
          if (keccak_f1600_count > 0) {
                  printf(" f1600: %lu", keccak_f1600_count);
          }
          printf(" )\n");
          */
  return tot;
}

